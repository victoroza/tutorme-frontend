(function () {
    'use strict';
    // alert()

    angular
        .module('app', ['ngRoute', 'ngCookies'])
        .config(config)
        .run(run);

    config.$inject = ['$routeProvider', '$locationProvider'];
    function config($routeProvider, $locationProvider) {
      $routeProvider
            .when('/', {
                // controller: 'HomeController',
                templateUrl: 'home/home.view.html',
                controllerAs: 'vm'
            })

            .when('/login', {
                controller: 'LoginController',
                templateUrl: 'login/login.view.html',
                controllerAs: 'vm'
            })

            .when('/register', {
                controller: 'RegisterController',
                templateUrl: 'register/register.view.html',
                controllerAs: 'vm'
            })

            .when('/users', {
                controller: 'UserController',
                templateUrl: 'users/users.view.html',
                controllerAs: 'vm'
            })

            .when('/user_profile/:userId', {
                templateUrl:function(params){
                  if(params.userId == localStorage.username){
                    console.log("Own profile");
                    return 'user_profile/editProfile.view.html'
                  }else if(localStorage.is_superuser == "true"){
                    console.log("Admin app.js");
                    return 'user_profile/adminEditProfile.view.html'
                  }else{
                    return 'user_profile/userId.view.html'
                  }
                },
            })

            .when('/schools', {
                controller: 'SchoolController',
                templateUrl: 'school/school.view.html',
                controllerAs: 'vm'
            })

            .when('/classes/:schoolId', {
                controller: 'ClassController',
                templateUrl: 'class/class.view.html',
                controllerAs: 'vm'
            })

            .when('/tutors/:schoolId/:classMajor/:classNumber', {
                controller: 'TutorController',
                templateUrl: 'tutors/tutors.view.html',
                controllerAs: 'vm'
            })

            .when('/appointment', {
                controller: 'AppointmentController',
                templateUrl: 'appointment/appointment.view.html',
                controllerAs: 'vm'
            })

            .when('/makeAppt/:tutorId/:classId', {
                controller: 'MakeApptController',
                templateUrl: 'appointment/makeAppt.view.html',
                controllerAs: 'vm'
            })

            .otherwise({ redirectTo: '/login' });
    }

    run.$inject = ['$rootScope', '$location', '$cookieStore', '$http'];
    function run($rootScope, $location, $cookieStore, $http) {
        // keep user logged in after page refresh
        if (!localStorage.token) {
            console.log("HERE");
            $location.path('/');
            return;
        }
    }
})();
